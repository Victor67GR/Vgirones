# Especificació de la tasca: Escriu una funció maxim(a, b, c) que retorni el nombre més gran entre els tres.
def maxim(a, b, c):
    return max(a, b, c)
print(maxim(3, 7, 5))
print(maxim(-1, -5, -3))
print(maxim(10, 10, 10))
print(maxim(0, 100, 50))  
