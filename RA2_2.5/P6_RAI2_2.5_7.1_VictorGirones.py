# Troba el màxim de trios de nombres. Defineix maxim(a, b, c). Troba el màxim de: (3, 7, 5) (10, 2, 8) (1, 1, 1)

def maxim(a, b, c):
    return max(a, b, c)     
print("El màxim de (3, 7, 5) és:", maxim(3, 7, 5))
print("El màxim de (10, 2, 8) és:", maxim(10, 2, 8))
print("El màxim de (1, 1, 1) és:", maxim(1, 1, 1))  
