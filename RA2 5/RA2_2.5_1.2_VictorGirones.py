# Crea un fitxer calculadora.py amb funcions de suma, resta, multiplicació i divisió.

def suma(a, b):
    return a + b

def resta(a, b):
    return a - b

def multiplica(a, b):
    return a * b

def divideix(a, b):
    return a / b

